package com.request.transformation.model;

public class AdditionalDetails {

	private String consentInformation;
	private String consentController;
	
	public String getConsentInformation() {
		return consentInformation;
	}
	public void setConsentInformation(String consentInformation) {
		this.consentInformation = consentInformation;
	}
	public String getConsentController() {
		return consentController;
	}
	public void setConsentController(String consentController) {
		this.consentController = consentController;
	}
	
}
